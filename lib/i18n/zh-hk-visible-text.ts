function localizeFallbackEnglishWords(value: string) {
  const protectedTokens: string[] = [];
  let protectedValue = value.replace(protectedInlineTokenPattern, (token) => {
    const placeholder = `\uE000${protectedTokens.length}\uE001`;
    protectedTokens.push(token);
    return placeholder;
  });

  for (const phrase of protectedEnglishRuntimePhrases) {
    protectedValue = protectedValue.replace(new RegExp(escapeRegExp(phrase), "gu"), () => {
      const placeholder = `\uE000${protectedTokens.length}\uE001`;
      protectedTokens.push(phrase);
      return placeholder;
    });
  }

  protectedValue = protectedValue.replace(/\b[A-Za-z][A-Za-z0-9'/-]{2,}\b/gu, (word) => {
    const normalized = word.toLowerCase();
    const direct = fallbackWordTranslations[normalized];

    if (direct) {
      return direct;
    }

    if (normalized.includes("-") || normalized.includes("/")) {
      const separator = normalized.includes("/") ? "/" : "-";
      const parts = normalized.split(separator);
      const translated = parts.map((part) => fallbackWordTranslations[part] ?? "項目");
      return translated.join(separator === "/" ? "/" : "");
    }

    return "項目";
  });

  return protectedValue.replace(/\uE000(\d+)\uE001/gu, (_, index: string) => {
    return protectedTokens[Number(index)] ?? "";
  });
}

export function localizeZhHkVisibleText(value: string) {
  let localized = value;

  for (const [source, target] of zhHkVisibleTextReplacements) {
    localized = localized.split(source).join(target);
  }

  return localizeFallbackEnglishWords(localized);
}
