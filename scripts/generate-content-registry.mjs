import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { generateI18nContentBundle } from "./generate-i18n-content-bundle.mjs";
import { generateContentVariantBundle } from "./generate-content-variant-bundle.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const contentRoot = path.join(repoRoot, "content");
const catalogRoot = path.join(contentRoot, "catalog");
const conceptsRoot = path.join(contentRoot, "concepts");
const outputFilePath = path.join(
  repoRoot,
  "lib",
  "content",
  "generated",
  "content-registry.ts",
);

const catalogEntries = [
  { key: "concepts", fileName: "concepts.json", importName: "conceptCatalogData" },
  {
    key: "guidedCollections",
    fileName: "guided-collections.json",
    importName: "guidedCollectionsCatalogData",
  },
  {
    key: "recommendedGoalPaths",
    fileName: "recommended-goal-paths.json",
    importName: "recommendedGoalPathsCatalogData",
  },
  {
    key: "starterTracks",
    fileName: "starter-tracks.json",
    importName: "starterTracksCatalogData",
  },
  { key: "subjects", fileName: "subjects.json", importName: "subjectsCatalogData" },
  { key: "testPacks", fileName: "test-packs.json", importName: "testPacksCatalogData" },
  { key: "topicTests", fileName: "topic-tests.json", importName: "topicTestsCatalogData" },
  { key: "topics", fileName: "topics.json", importName: "topicsCatalogData" },
];

function toCamelCase(value) {
  return value.replace(/-([a-z0-9])/g, (_, char) => char.toUpperCase());
}

function getConceptImportName(fileName) {
  return `${toCamelCase(fileName.replace(/\.json$/u, ""))}ConceptData`;
}

function getPosixRelativePath(...segments) {
  return path.posix.join(...segments);
}

function formatObjectLiteral(entries) {
  return entries
    .map(({ key, value }) => `  ${JSON.stringify(key)}: ${value},`)
    .join("\n");
}

function readJsonFiles(directoryPath) {
  return fs
    .readdirSync(directoryPath)
    .filter((fileName) => fileName.endsWith(".json"))
    .sort((left, right) => left.localeCompare(right));
}

function getPosixRepoRelativePath(filePath) {
  return path.relative(repoRoot, filePath).split(path.sep).join(path.posix.sep);
}

function getGitModifiedTimes(filePaths) {
  const repoRelativePaths = filePaths.map((filePath) => getPosixRepoRelativePath(filePath));
  const repoRelativePathSet = new Set(repoRelativePaths);
  const modifiedTimes = new Map();

  try {
    const output = execFileSync(
      "git",
      ["log", "--format=__COMMIT__:%cI", "--name-only", "--", ...repoRelativePaths],
      {
        cwd: repoRoot,
        encoding: "utf8",
        maxBuffer: 20 * 1024 * 1024,
        stdio: ["ignore", "pipe", "ignore"],
      },
    );

    let currentCommitTime = null;

    for (const line of output.split(/\r?\n/u)) {
      if (!line) {
        continue;
      }

      if (line.startsWith("__COMMIT__:")) {
        currentCommitTime = line.slice("__COMMIT__:".length);
        continue;
      }

      if (currentCommitTime && repoRelativePathSet.has(line) && !modifiedTimes.has(line)) {
        modifiedTimes.set(line, new Date(currentCommitTime).toISOString());
      }
    }
  } catch {
    return modifiedTimes;
  }

  return modifiedTimes;
}

function getIsoModifiedTime(filePath, gitModifiedTimes) {
  return gitModifiedTimes.get(getPosixRepoRelativePath(filePath)) ?? fs.statSync(filePath).mtime.toISOString();
}

const conceptFiles = readJsonFiles(conceptsRoot);
const catalogFilePaths = catalogEntries.map((entry) => path.join(catalogRoot, entry.fileName));
const conceptFilePaths = conceptFiles.map((fileName) => path.join(conceptsRoot, fileName));
const gitModifiedTimes = getGitModifiedTimes([...catalogFilePaths, ...conceptFilePaths]);

const lines = [
  "// This file is auto-generated by scripts/generate-content-registry.mjs.",
  "// Do not edit it manually.",
  "",
];

for (const entry of catalogEntries) {
  const importPath = getPosixRelativePath("../../../content/catalog", entry.fileName);
  lines.push(`import ${entry.importName} from "${importPath}";`);
}

for (const fileName of conceptFiles) {
  const importName = getConceptImportName(fileName);
  const importPath = getPosixRelativePath("../../../content/concepts", fileName);
  lines.push(`import ${importName} from "${importPath}";`);
}

lines.push("");
lines.push("export const catalogData = {");
lines.push(
  formatObjectLiteral(
    catalogEntries.map((entry) => ({
      key: entry.key,
      value: entry.importName,
    })),
  ),
);
lines.push("} as const;");
lines.push("");
lines.push("export const catalogFilePaths = {");
lines.push(
  formatObjectLiteral(
    catalogEntries.map((entry) => ({
      key: entry.key,
      value: JSON.stringify(getPosixRelativePath("content/catalog", entry.fileName)),
    })),
  ),
);
lines.push("} as const;");
lines.push("");
lines.push("export const catalogLastModified = {");
lines.push(
  formatObjectLiteral(
    catalogEntries.map((entry) => ({
      key: entry.key,
      value: JSON.stringify(getIsoModifiedTime(path.join(catalogRoot, entry.fileName), gitModifiedTimes)),
    })),
  ),
);
lines.push("} as const;");
lines.push("");
lines.push("export const conceptContentData = {");
lines.push(
  formatObjectLiteral(
    conceptFiles.map((fileName) => ({
      key: fileName.replace(/\.json$/u, ""),
      value: getConceptImportName(fileName),
    })),
  ),
);
lines.push("} as const;");
lines.push("");
lines.push("export const conceptContentFilePaths = {");
lines.push(
  formatObjectLiteral(
    conceptFiles.map((fileName) => ({
      key: fileName.replace(/\.json$/u, ""),
      value: JSON.stringify(getPosixRelativePath("content/concepts", fileName)),
    })),
  ),
);
lines.push("} as const;");
lines.push("");
lines.push("export const conceptContentLastModified = {");
lines.push(
  formatObjectLiteral(
    conceptFiles.map((fileName) => ({
      key: fileName.replace(/\.json$/u, ""),
      value: JSON.stringify(getIsoModifiedTime(path.join(conceptsRoot, fileName), gitModifiedTimes)),
    })),
  ),
);
lines.push("} as const;");
lines.push("");

const nextContent = `${lines.join("\n")}`;
const previousContent = fs.existsSync(outputFilePath)
  ? fs.readFileSync(outputFilePath, "utf8")
  : null;

if (previousContent !== nextContent) {
  fs.mkdirSync(path.dirname(outputFilePath), { recursive: true });
  fs.writeFileSync(outputFilePath, nextContent, "utf8");
}

const i18nBundle = generateI18nContentBundle();
const contentVariantBundle = generateContentVariantBundle(repoRoot);

console.log(
  `Generated ${path.relative(repoRoot, outputFilePath)} with ${catalogEntries.length} catalogs and ${conceptFiles.length} concept files.`,
);
console.log(
  `Generated i18n content bundle for ${i18nBundle.localeCount} locale${
    i18nBundle.localeCount === 1 ? "" : "s"
  }.`,
);
console.log(
  `Generated content variant manifest for ${Object.keys(contentVariantBundle.manifest.concepts).length} concept${
    Object.keys(contentVariantBundle.manifest.concepts).length === 1 ? "" : "s"
  }.`,
);
